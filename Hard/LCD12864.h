#define DATABUS P0  //显示屏数据线
#define ON 0x3F //显示开
#define OFF 0x3E //显示关
#define X 0xB8 //页地址  一共64行 8行为一页 共8页
#define Y 0x40 //列地址  一共128列 左右半屏个64列
#define Z 0xC0 //行地址
#define CLEAR 0x00 
#define CMD 0 //命令
#define DAT 1 //数据
#define LEFT 0//左半屏
#define RIGHT 1 //右半屏
#define ALL 2 //左右屏全选

sbit RS = P1^5;  //低电平写命令 高电平写数据
sbit RW = P4^4;  //读写 低电平写 高电平读
sbit EN = P4^5;  //使能
sbit CSR= P5^4;  //右半屏
sbit CSL= P4^1;  //左半屏

/**
 * @brief 判断是否为忙
 * 
 */
static void Busy(void);

/**
 * @brief 往LCD12864里写数据或命令
 * 
 * @param rs CMD写命令 DAT写数据
 * @param lr LEFT左半屏 RIGHT右半屏 ALL左右屏全选
 * @param cd 写入的数据或命令
 */
static void writeLCD(uint8_t rs, uint8_t lr, uint8_t cd);

/**
 * @brief 清屏函数 即将每个像素点设置为0x00
 * 
 */
