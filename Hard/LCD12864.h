/**
 * @brief LCD12864初始化函数 
 * 
 */
void LCDInit(void);

/**
 * @brief 显示任意宽度的字体
 * 
 * @param xx 页数 一页为8行
 * @param yy 列数 最大值为63 超过63时会变为0
 * @param word 数组指针
 * @param length 字体宽度
 */
